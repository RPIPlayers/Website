            <p>The current Playhouse, a former U.S.O. building, was moved from Windsor Locks, Connecticut to Rensselaer in 1946. The building was acquired by the Institute to accommodate a library extension, study space and social activities. The structure was placed along 15th Street, just south of the Quadrangle Dorms and Russell Sage Jr. Dining Hall, and thus dubbed the 15th Street Lounge or Lounge 15. The building was first inhabited by the RPI Players during the 1948 - 1949 and 1949 - 1950 seasons while repairs were performed on the Old Gym. The 15th Street Lounge became the permanent home of the R.P.I. Players in the middle of the 1965 - 1966 season with the production of A Slow Dance On The Killing Ground. The building was shared by RPI Players and other campus organizations including WRPI, the Outing Club, the Fencing Club, the International Club, the Band, Orchestra and Glee Clubs for concerts and recitals as well as a location for worship on Sundays.</p>
            
            <p>The RPI Players found it necessary to modify the interior spaces of the building to suit their needs. Modifications of the facility continued until the 1982 - 1983 season when a building renovation commenced. Displaced from their home, the RPI Players managed to still produce a season of shows by performing in other campus locations including the McNeil Room of the Rensselaer Union and the West Hall auditorium. In November of 1983, the newly renovated RPI Playhouse opened with a production of West Side Story. Some additional spaces, like the costume shop and scene shop, were added during the renovation, expanding the footprint of the building.</p>
            
            <p>Today, the RPI Players has staged over 300 productions and is nearing its 90th Season. The troupe currently produces three mainstage productions each year, and works on a variety of collaborative projects.</p>
